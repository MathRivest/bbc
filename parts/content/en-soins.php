                            <p>Orthodontic treatment is done for various reasons: to straighten crooked or misaligned teeth, to correct malocclusions, or to improve the appearance of your smile. There are conventional treatments, such as metallic or transparent braces, or the innovative Invisalign technology, which straightens the teeth without visible braces, clips or wires. Invisalign uses three-dimensional computer technology to sketch-out the entire treatment process from beginning to end. Using this plan, a series of custom-made, transparent and removable retainers (aligners) are manufactured. Each retainer in the series is worn for approximately two weeks up until the teeth gradually move to their final position.</p>

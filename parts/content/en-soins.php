                            <i class="icon-caret-right"></i>
                        </a>
                        <div class="m-accordion-content">
                            <p>Dental fillings are used to repair damage created by decay and to prevent the tooth from further deterioration. This procedure involves inserting a material into the damaged dental cavity. Several materials are available: an amalgam, a composite, a CEREC ceramic or a silver filling (this material is no longer used in our clinic due to the toxicity of mercury). The most importance factors in treating cavities are choosing the appropriate material and determining the best treatment. Your dentist will help guide you to make the decision that best suits your needs.</p>

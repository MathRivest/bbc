                            Endodontics (Root Canal)
                            <i class="icon-caret-right"></i>
                        </a>
                        <div class="m-accordion-content">
                            <p>It is important to keep one’s own teeth as long as possible. Root canal therapy is a wise choice to save a tooth that would otherwise require extraction. Here are some examples where root canal treatment is required: in the case of deep cavities that reach the pulp (the living portion of the tooth), a dental infection or severe pain. Root canal treatment involves removing the inflamed pulp or infected tooth, followed by cleaning and disinfecting the inner portion of the tooth known as the canal.</p>
                        </div>

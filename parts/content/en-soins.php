                            <p>Wisdom teeth may become problematic during adolescence because they can cause pain and infection, or push and move the surrounding teeth. In most cases, it is advisable to have them removed.</p>
                            <p>There are two types of gingival grafts (gum grafts). Autogenous grafts involve transplanting tissue from the palate to the affected site. Allogenic transplantation involves covering the chosen site with a collagen membrane. A gingival graft is usually recommended to treat gum recession, which occurs when the height of the gum decreases and the root of the tooth becomes exposed.</p>

                        <a href="#">
                            Veneer, Crown and Bridge
                            <i class="icon-caret-right"></i>
                        </a>
                        <div class="m-accordion-content">
                            <p>A veneer is a thin shell made from a composite or ceramic material that covers the front of the teeth. It is a cosmetic treatment, which improves the appearance of your smile.</p>
                            <p>A crown is used to restore the shape, appearance and the function of a damaged or decayed tooth. The crown is made of ceramic as this material most resembles a natural tooth. CEREC technology is used to make the crown and can be done either in-office or in the laboratory.</p>
                            <p>The bridge replaces one or more missing teeth. When a tooth is lost, it must be replaced as soon as possible to prevent the surrounding teeth from moving. A bridge is made of crowns that have been soldered together and then attached to the adjacent teeth.</p>
                        </div>
                    </li>

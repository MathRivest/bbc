                            Periodontics (Treatment of Gum Disease)
                            <i class="icon-caret-right"></i>
                        </a>
                        <div class="m-accordion-content">
                            <p>The term "periodontal disease" includes all diseases affecting the tissues supporting the teeth. Gingivitis, for example, will present as bleeding of the gums during brushing, gum swelling and/or pain and persistent bad breath. Gum inflammation should be treated quickly to avoid the possibility of periodontal disease as tissues supporting the teeth will be affected. Periodontal disease is the main cause of adult tooth loss and can lead to other health problems if left untreated (cardiovascular diseases and premature births). With proper periodontal treatment, the health of the gums can be restored.</p>
                        </div>
                    </li>
